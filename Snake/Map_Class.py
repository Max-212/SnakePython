    #Конструктор
    def __init__(self):
        #Карта-список 1-го уровня
        self.level = self.level1              
        #Загрузка заднего фона
        self.map_img=pygame.image.load("images/map.png")
        #Список границ
        self.barrier = []
        #Список свободной области
        self.free = []
        #Выбор блоков и заднего фона
        self.block_img = random.choice(self.blocks)
        self.ground_img = random.choice(self.grounds)

    
    #Смена уровня
    def changeLevel(self, newLevel):
        self.level = newLevel
        self.block_img = random.choice(self.blocks)
        self.ground_img = random.choice(self.grounds)
        self.create_img()
        self.map_img=pygame.image.load("images/map.png")
        self.barrier = []
        self.free = []
        self.filter()
        

    #Генерация картинки карты по списку уровня
    def create_img(self):
        screen = pygame.Surface((441,441),pygame.SRCALPHA,32)
        x = 1 
        y = 1
        for i in self.level:
            if i == 0:
                block_rect = pygame.Rect(x,y,11,11)
                screen.blit(self.block_img,block_rect)
            x+=11
            if x >= 441:
                y+=11
                x=1
        pygame.image.save(screen,"images/map.png")


    #Отрисовка карты на окне
    def draw_map(self,window):
        window.blit(self.map_img,(0,0))


    #Сортирует карту-список на списки границ и свободного пространства
    def filter(self):
        x = 1
        y = 1
        for i  in self.level:
            if i == 0:
                self.barrier.append([x,y])
            elif i == 1 and y != 12:
                self.free.append([x,y])
            x += 11
            if x == 441:
                y += 11
                x = 1

